// Examples

// Arithematic Operators
let a = 5;
let b = 2;
let sum = a + b; // Addition
let difference = a - b; // Subtraction
let product = a * b; // Multiplication
let quotient = a / b; // Division

// Comparision Operators
let x = 10;
let y = 5;
let isEqual = x === y; // Strict equality
let isGreaterThan = x > y; // Greater than

// Logical Operators
let isTrue = true;
let isFalse = false;
let logicalAnd = isTrue && isFalse; // Logical AND
let logicalOr = isTrue || isFalse; // Logical OR
let logicalNot = !isTrue; // Logical NOT

// Assignment Operators
let a2 = 10;
a2 += 5; // Equivalent to a = a + 5

// Unary Operators
let count = 0;
count++; // Increment by 1
count--; // Decrement by 1
